  <p class="mater2">Виды издержек и их названия</p>
  <p class="materText">
    <b>Общие издержки</b> (общие затраты, валовые издержки) — Total Cost ($TC$) — издержки, которые должны быть понесены для производства $q$ единиц продукции. Подразделяются на переменные и постоянные издержки.
    <big> $$TC=VC+FC$$ </big>
    <br/> <b>Переменные издержки</b> (переменные затраты) — Variable Cost ($VC$) — затраты, величина которых зависит от объема продукции. 
    <br/> <b>Постоянные издержки</b> (постоянные затраты) — Fixed Cost ($FC$) — затраты, величина которых не зависит от объема продукции, издержки понесенные при отсутствии производства (производстве нуля единиц товара).
    <br/> Так, например, для функции общих издержек $TC=q^2+q+12$ постоянные издержки равны 12, переменные издержки равны $q^2+q$.
    <br/> Классическим примером функции издержек является функция с картинки:
    <img class="materImg2" src="images/material/material-costs/1.png" alt="">
    <br/> Общие, переменные и постоянные издержки измеряются в денежных единицах. При этом часто во многих задачах вам необходимо работать со средними и предельными издержками.
    <br/> <b>Средние издержки (общие средние издержки)</b> — Average Cost ($AC$ или $ATC$) — общие издержки на единицу продукции, то есть:
    <big> $$AC=\frac{TC}{q}=AVC+AFC$$ </big>
    <b>Средние переменные издержки</b> — Average Variable Cost ($AVC$) — переменные издержки на единицу продукции.
    <big> $$AVC=\frac{VC}{q}$$ </big>
    <b>Средние постоянные издержки</b> — Average Fixed Cost ($AFC$) — постоянные издержки на единицу продукции.
    <big> $$AFC=\frac{FC}{q}$$ </big>
    Так, например, для функции общих издержек $TC=q^2+q+12$ средние издержки — $AC=q+1+\frac{12}{q}$, $AVC=q+1$, $AFC=\frac{12}{q}$.
