  <p class="materText">
  В функционирования модели существуют следующие предпосылки:
  <br/>&nbsp; &nbsp; –) На рынке много продавцов и покупателей;
  <br/>&nbsp; &nbsp; –) Однородность и делимость продаваемой продукции;
  <br/>&nbsp; &nbsp; –) Отсутствие барьеров для входа или выхода с рынка;
  <br/>&nbsp; &nbsp; –) Высокая мобильность факторов производства;
  <br/>&nbsp; &nbsp; –) Равный и полный доступ всех участников к информации (цены товаров);
  <br/>&nbsp; &nbsp; –) Определенные права собствености.
  <br/>
  Так как ни один из агентов не имеет рыночной власти, из-за того, что агентов очень много и продукция на рынке однородна, то один производитель может продать любое количество по рыночной цене. Другими словами спрос для каждой фирмы абсолютно эластичен и равен $P$ равновесной.</p>
  <img class="materImg2" src="images/material/material-perfect-competition/2.png" alt="">
  <p class="materText">
  На таком спросе (он изображен справа) каждая фирма максимизирует прибыль. Прибыль фирмы всегда выглядит как
  <big> $$\pi=TR-TC=P_d\times q-TC,$$ </big>
  где $TR$ — общая выручка, $TC$ — общие издержки, $P_d$ — цена спроса на продукцию фирмы, $q$ — количество продукции производимое одной фирмой.
  <br/> Если переписать то же самое в виде функции от $q$, то мы получим:
  <big> $$\pi(q)=P_d(q)\times q-TC(q)=P\times q-TC(q)$$ </big>
  Последнее равенство следует из того факта, что для фирмы цена —— константа, равная равновесной цене, но при этом фирма не знает, какая цена установится на рынке, поэтому ей необходимо оптимизировать функцию прибыли при каждом возможном значении цены. Чтобы найти оптимальное количества при каждом возможном значении цены, возьмем производную от прибыли и приравняем ее к нулю. (Это необходимое, но не достаточное условие существования максимума, но, допустим, он существует)
  <big> $$\pi(q)\to \max\Rightarrow \pi'=P-MC(q)=0\Rightarrow P=MC(Q)$$ </big>
  Мы получили не что иное, как предложение (но не совсем) одной фирмы (только немного в непривычном виде, обычно предложение это функция $q$ от $P$, здесь мы получили его в вывернутой форме). Мы нашли количество, при котором для определенной цены достигается максимум прибыли, но, возможно, цена на рынке установится настолько низкая, что фирме будет выгодно уйти с рынка. Фирма уходит с рынка, если 
  <big> $$\pi<\pi(0)=-FC$$ </big>
  так как если это неравенство выполняется, то фирме выгоднее уйти с рынка (то есть производить $q=0$), чем производить . Тогда фирма не уходит если
  <big> $$P\times q-VC(q)-FC\geqslant -FC\Rightarrow P\times q-VC(q)\geqslant 0\Rightarrow p\geqslant AVC(q)\Rightarrow MC(q)\geqslant AVC(q)$$ </big>
  Как мы видим, предложение — это участок  $MC$, в котором выполняется неравенство $MC\geqslant AVC$. Докажем, что функции $MC(q)$ и $AVC(q)$ пересекаются в точке минимума $AVC(q)$. Допустим точка минимума для непрерывной функции $AVC(q)$ существует и в ней $q>0$, тогда производная $AVC(q)$ в этой точке равна нулю.
  <big> $$AVC'(q)=0\Leftrightarrow \left(\frac{VC}{q}\right)'=\frac{VC'(q)\times q-VC}{q^2}=0\Rightarrow MC=\frac{VC}{q}$$ </big>
  Когда это неравенство выполняется, фирме выгоднее уйти с рынка (производить $q = 0$), чем производить $q>0$. (Докажите самостоятельно, что $MC$ так же проходят через минимум $AC$, это пересечение будет точкой, в которой прибыль фирмы равна нулю).
  <br/> Теперь мы можем переписать полученное ранее предложение как участок $MC$ где $MC\geqslant AVC$ — именно в таком виде обычно и записывается предложение.
