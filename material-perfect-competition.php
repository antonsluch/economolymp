  <p class="materText">
  Что такое равновесие? Равновесие — ситуация на рынке, когда спрос на товар равен его предложению. Мы уже видели, как выводится предложение, рыночный спрос выводится на онлайн-курсах по экономике <a href="https://vk.com/economolymp">Экономический Олимп</a>.
  <br/> Но каким образом система достигает положения равновесия?
  <img class="materImg2" src="images/material/material-perfect-competition/1.png" alt="">
  Допустим, первоначальное значение цены — $P_1$. По этой цене производители готовы продать $Q_1$ единиц товара, а потребители хотят купить $Q_2 > Q_1$ единиц товара. Возникает избыточный спрос $ВС = Q_2 - Q_1$. Потребители начинают конкурировать между собой за обладание товаром, и вследствие этого цена повышается. Процесс "нащупывания" равновесной цены идет непрерывно до тех пор, пока избыточный спрос не становится равным нулю в точке — $Е$. 
  <br/>Аналогично, если первоначальное значение цены - $Р_2$, возникает избыточное предложение $AL$; конкуренция среди продавцов понижает цену, пока избыточное предложение не становится равно нулю в точке $Е$. Таким образом, в точке $Е$ на рынке устанавливается равновесие, так как здесь нет ни избыточного спроса, ни избыточного предложения, а значит и нет стимулов к изменению цены.
