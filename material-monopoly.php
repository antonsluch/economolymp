  <p class="materText">
  <b>Монополия</b> — это рыночная структура, при которой крупное предприятие контролирует производство и сбыт одного или нескольких видов продукции, а значит конкуренция производителей отсутствует — рыночная власть в руках производителя (поэтому он может <b>назначать цену</b>).
  <br/>
  Причины возникновения монополий (предпосылки модели):
  <br/>–) Высокая доля постоянных издержек, которые требуют одномоментных крупных вложений создания бизнеса и в случае появления конкуренции не окупаются;
  <br/>–) Законодательные барьеры для осуществления деятельности (лицензирование, сертификация);
  <br/>–) Внешнеэкономическая политика. Она направлена на защиту рынка от иностранной конкуренции с целью поддержать отечественные субъекты. Включает: обложение пошлинами иностранных товаров, ограничение ввоза или запрет;
  <br/>–) Поглощение и слияние фирм (когда более крупная покупает более мелкую фирму, устраняя конкуренцию на рынке).
  <br/> Решим задачу фирмы в рамках модели монополии. Прибыль фирмы это:
  <big> $$\pi=TR-TC=P_d\times q-TC(q),$$</big>
  где $TR$ — общая выручка, $TC$ — общие издержки, $P_d$ — цена спроса на продукцию фирмы, $q$ — количество продукции производимое одной фирмой. И фирма максимизирует прибыль подставляя спрос на свою продукцию равный рыночному спросу (фирма единственный производитель на рынке, а значит весь рыночный спрос — спрос на его продукцию). 
  <br/>
  Максимизационная задача фирмы это
  <big> $$\pi=P_d(q)\times q-TC(q)\to \max$$</big>
  Допустим максимум этой функции существует (для нахождения максимума всегда проверяйте вторую производную!), тогда в нем выполняется
  <big> $$\pi=P_d\times q-TC(q),$$</big>
  <big> $$\pi'=MR-MC$$</big>
  Как мы видим для нахождения оптимального выпуска можно либо взять производную по количеству и приравнять ее к нулю, либо приравнять $MR(q)$ и $MC(q)$. Но этого не достаточно, необходимо всегда удостоверяться, что мы нашли максимум, а не минимум. Для этого либо производная прибыли должна убывать по количеству, либо MR должно быть больше MC в окрестности найденого решения.
